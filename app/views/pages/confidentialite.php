<p class="text-text-muted text-sm mb-6">Cette politique explique comment Variable collecte, utilise et protège tes données personnelles. Elle est conforme au Règlement Général sur la Protection des Données (RGPD européen) et à la Loi 25 du Québec.</p>

<h2>1. Responsable du traitement</h2>
<p><strong>Les éditions Variable</strong>, fondateur Angello Luvungu Muhiya, siège opérationnel à Trois-Rivières (Québec, Canada).</p>
<p>Pour toute question : <a href="mailto:contact@leseditionsvariable.com" class="text-accent">contact@leseditionsvariable.com</a></p>

<h2>2. Données collectées</h2>
<h3>2.1 Inscription et compte</h3>
<ul>
    <li>Email (obligatoire)</li>
    <li>Prénom, nom (obligatoires)</li>
    <li>Mot de passe (chiffré, jamais lisible en clair)</li>
    <li>Téléphone (optionnel, utile pour Mobile Money)</li>
    <li>Pays, devise préférée</li>
    <li>Photo de profil (optionnelle)</li>
</ul>

<h3>2.2 Navigation</h3>
<p>Cookies de session (nécessaires au fonctionnement du site). Données analytiques anonymisées (pages visitées, durée de session) si tu donnes ton consentement.</p>

<h3>2.3 Paiement</h3>
<p>Identifiants de transaction (jamais le numéro de carte). Méthode utilisée (Stripe ou Money Fusion). Montant et devise.</p>

<h3>2.4 Lecture</h3>
<p>Progression dans les livres, favoris, historique des livres consultés. Ces données nous permettent de te proposer des recommandations et de redistribuer équitablement les revenus aux auteurs.</p>

<h2>3. Finalités du traitement</h2>
<ul>
    <li>Fourniture du service (création de compte, lecture, paiements)</li>
    <li>Communication avec toi (emails transactionnels, support)</li>
    <li>Amélioration du produit (analytics anonymes)</li>
    <li>Respect des obligations légales (comptabilité, fiscalité)</li>
    <li>Si tu y consentes : envoi de la newsletter</li>
</ul>

<h2>4. Base légale</h2>
<p>Le traitement repose sur :</p>
<ul>
    <li><strong>Exécution du contrat</strong> : pour fournir le service que tu as souscrit</li>
    <li><strong>Consentement</strong> : pour la newsletter et les analytics</li>
    <li><strong>Intérêt légitime</strong> : pour la sécurité du service et la prévention de la fraude</li>
    <li><strong>Obligation légale</strong> : pour la conservation comptable</li>
</ul>

<h2>5. Destinataires des données</h2>
<p>Tes données sont partagées uniquement avec nos sous-traitants techniques, sous engagement de confidentialité :</p>
<ul>
    <li><strong>Stripe</strong> (USA) : traitement des paiements par carte</li>
    <li><strong>Money Fusion</strong> (Côte d'Ivoire) : traitement des paiements Mobile Money</li>
    <li><strong>Resend</strong> ou équivalent : envoi des emails transactionnels</li>
    <li><strong>NitroHost</strong> : hébergement des serveurs</li>
</ul>
<p>Aucune donnée n'est revendue ni partagée avec des tiers à des fins publicitaires.</p>

<h2>6. Durée de conservation</h2>
<p>Données de compte actif : tant que ton compte est ouvert. Après suppression de compte : anonymisation immédiate, conservation des transactions à des fins comptables pendant <strong>5 ans</strong>. Données analytiques : <strong>13 mois</strong>. Logs de sécurité : <strong>1 an</strong>.</p>

<h2>7. Tes droits</h2>
<p>Conformément au RGPD et à la Loi 25, tu disposes des droits suivants :</p>
<ul>
    <li><strong>Accès</strong> : obtenir une copie de tes données</li>
    <li><strong>Rectification</strong> : corriger des informations inexactes</li>
    <li><strong>Suppression</strong> (« droit à l'oubli »)</li>
    <li><strong>Opposition</strong> au traitement</li>
    <li><strong>Portabilité</strong> : recevoir tes données dans un format structuré</li>
    <li><strong>Limitation</strong> du traitement</li>
</ul>

<h2>8. Comment exercer tes droits</h2>
<p>Écris-nous à <a href="mailto:contact@leseditionsvariable.com" class="text-accent">contact@leseditionsvariable.com</a> avec « DROITS RGPD » en objet. Nous répondons sous <strong>30 jours</strong> maximum.</p>
<p>Pour la suppression : tu peux le faire toi-même depuis « Mon compte » → « Mon profil » → « Zone de danger ».</p>

<h2>9. Cookies</h2>
<p>Nous utilisons :</p>
<ul>
    <li><strong>Cookies de session</strong> (nécessaires) : authentification, panier</li>
    <li><strong>Cookie de consentement</strong> : mémorise tes préférences cookies</li>
    <li><strong>Cookies analytiques anonymes</strong> (avec ton consentement uniquement)</li>
</ul>

<h2>10. Transferts hors UE</h2>
<p>Certains de nos sous-traitants (Stripe, Money Fusion) opèrent hors de l'Union européenne. Ces transferts sont encadrés par des clauses contractuelles types et garanties contractuelles équivalentes.</p>

<h2>11. Sécurité</h2>
<p>Mesures techniques en place : HTTPS partout, hashage des mots de passe (bcrypt), sauvegardes quotidiennes chiffrées, accès restreint aux serveurs (clés SSH uniquement), revues de sécurité régulières.</p>
<p>En cas de violation de données affectant tes informations personnelles, nous nous engageons à te notifier dans un délai de 72 heures conformément au RGPD.</p>

<h2>12. Modification de la politique</h2>
<p>Nous pouvons mettre à jour cette politique. Les changements substantiels font l'objet d'une notification par email.</p>

<h2>13. Contact</h2>
<p>Pour toute question relative à tes données : <a href="mailto:contact@leseditionsvariable.com" class="text-accent">contact@leseditionsvariable.com</a></p>

